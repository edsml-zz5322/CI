/**
 * @file mymath.cpp
 * @brief A simple math library for basic arithmetic operations.
 */

#include "mymath.hpp"

int add(int a, int b) {
    return a + b;
}

int subtract(int a, int b) {
    return a - b;
}

int multiply(int a, int b) {
    return a * b;
}

int divide(int a, int b) {
    return a / b;
}

